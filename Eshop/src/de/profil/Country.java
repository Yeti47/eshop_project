package de.profil;

public class Country {
	
	// Fields
	
	private String _name;
	private String _code;
	
	// Constructors
	
	public Country(String name, String code) {
		
		_name = name;
		_code = code;
		
	}

	// Getters / Setters
	
	public String getName() {
		return _name;
	}

	public String getCode() {
		return _code;
	}
	
	// Methods

}
